    public void ProcedimientoAVer(String recetaSelect) {
        File fileProc = null;

        try {
            String rutaCarpeta = carpetaPrincipal + "\\" + recetaSelect;
            String rutaProc = rutaCarpeta + "\\Procedimiento.txt";
            fileProc = new File(rutaProc);

            String nombRec = recetaSelect;
            String procRec = new String(Files.readAllBytes(Paths.get(rutaProc)));

            recetaActiva = new RecetaClase(nombRec, null, null, procRec);

        } catch (IOException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void EditarProcedimiento(String receta) {
        String ruta = carpetaPrincipal + "\\" + receta;
        try {
            FileWriter recipeP = new FileWriter(ruta + "\\Procedimiento.txt", false);
            recipeP.write(jtaProcedimiento.getText());
            recipeP.close();
        } catch (IOException ex) {
            Logger.getLogger(HoraDelPostre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

